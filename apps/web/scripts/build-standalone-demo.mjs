import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const webDir = path.resolve(__dirname, "..");
const standaloneDir = path.join(webDir, "dist-standalone");
const sourceHtmlPath = path.join(standaloneDir, "demo.html");
const outputHtmlPath = path.resolve(webDir, "../../docs/mockups/keeppage-full-frontend-demo.html");

function escapeScriptContent(source) {
  return source.replace(/<\/script/gi, "<\\/script");
}

async function inlineAssetReferences(html) {
  let output = html.replace(/<link rel="modulepreload"[^>]*>/g, "");

  const stylesheetMatches = [...output.matchAll(/<link rel="stylesheet"[^>]*href="([^"]+)"[^>]*>/g)];
  for (const match of stylesheetMatches) {
    const href = match[1];
    if (!href) {
      continue;
    }
    const assetPath = path.resolve(standaloneDir, href);
    const css = await fs.readFile(assetPath, "utf8");
    output = output.replace(match[0], () => `<style>\n${css}\n</style>`);
  }

  const scriptMatches = [...output.matchAll(/<script type="module"[^>]*src="([^"]+)"[^>]*><\/script>/g)];
  for (const match of scriptMatches) {
    const src = match[1];
    if (!src) {
      continue;
    }
    const assetPath = path.resolve(standaloneDir, src);
    const js = await fs.readFile(assetPath, "utf8");
    output = output.replace(
      match[0],
      () => `<script type="module">\n${escapeScriptContent(js)}\n</script>`,
    );
  }

  return output;
}

async function main() {
  const sourceHtml = await fs.readFile(sourceHtmlPath, "utf8");
  const inlinedHtml = await inlineAssetReferences(sourceHtml);
  const banner = "<!-- Generated by apps/web/scripts/build-standalone-demo.mjs. Do not edit by hand. -->\n";
  await fs.mkdir(path.dirname(outputHtmlPath), { recursive: true });
  await fs.writeFile(outputHtmlPath, `${banner}${inlinedHtml}`, "utf8");
  console.log(`Standalone demo written to ${outputHtmlPath}`);
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
